#!/usr/bin/env node
// macOS only: generate a tiny launcher app at ~/Applications/Ash.app so Ash
// shows up in Spotlight and Raycast and can be double-clicked. It doesn't bundle
// anything — it just runs the repo's Electron with this checkout, exactly like
// `npm start`. Idempotent; re-run after moving the repo. Run via `npm run install-app`.

import { execFileSync } from "node:child_process";
import { createRequire } from "node:module";
import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

if (process.platform !== "darwin") {
  console.log("make-macos-app: macOS only — nothing to do on this platform.");
  process.exit(0);
}

// The repo's bundled Electron. require("electron") returns the absolute path to
// the executable inside node_modules — the same binary `electron .` runs.
const require = createRequire(import.meta.url);
let electronBin;
try {
  electronBin = require("electron");
} catch {
  electronBin = null;
}
if (typeof electronBin !== "string" || !fs.existsSync(electronBin)) {
  console.error("Electron isn't installed yet — run `npm install` (or `npm run setup`) first.");
  process.exit(1);
}

const pkg = JSON.parse(fs.readFileSync(path.join(ROOT, "package.json"), "utf8"));
const APP_NAME = pkg.productName || "Ash";
const VERSION = pkg.version || "0.0.0";

const appDir = path.join(os.homedir(), "Applications", `${APP_NAME}.app`);
const contents = path.join(appDir, "Contents");
const macos = path.join(contents, "MacOS");
const resources = path.join(contents, "Resources");

// Rebuild from scratch so re-runs stay clean (no stale files from a moved repo).
fs.rmSync(appDir, { recursive: true, force: true });
fs.mkdirSync(macos, { recursive: true });
fs.mkdirSync(resources, { recursive: true });

// Info.plist — CFBundleIconFile "icon" resolves to Resources/icon.icns.
const infoPlist = `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<dict>
  <key>CFBundleName</key><string>${APP_NAME}</string>
  <key>CFBundleDisplayName</key><string>${APP_NAME}</string>
  <key>CFBundleExecutable</key><string>${APP_NAME}</string>
  <key>CFBundleIdentifier</key><string>ai.allthedata.ash</string>
  <key>CFBundleIconFile</key><string>icon</string>
  <key>CFBundlePackageType</key><string>APPL</string>
  <key>CFBundleShortVersionString</key><string>${VERSION}</string>
  <key>CFBundleVersion</key><string>${VERSION}</string>
  <key>NSHighResolutionCapable</key><true/>
</dict>
</plist>
`;
fs.writeFileSync(path.join(contents, "Info.plist"), infoPlist);

// The launcher: run the repo's Electron with this checkout as the app. Paths are
// baked in, so moving the repo means re-running `npm run install-app`.
const launcher = `#!/bin/bash
# Auto-generated by scripts/make-macos-app.mjs — launches Ash from its source
# checkout. If you move the repo, re-run: npm run install-app
exec "${electronBin}" "${ROOT}" "$@"
`;
const launcherPath = path.join(macos, APP_NAME);
fs.writeFileSync(launcherPath, launcher);
fs.chmodSync(launcherPath, 0o755);

// Icon (optional — bundle still works without it).
const icns = path.join(ROOT, "assets", "icon.icns");
if (fs.existsSync(icns)) {
  fs.copyFileSync(icns, path.join(resources, "icon.icns"));
}

// Register with Launch Services so Spotlight/Raycast index it immediately,
// rather than waiting for the periodic reindex. Best-effort.
const lsregister =
  "/System/Library/Frameworks/CoreServices.framework/Frameworks/LaunchServices.framework/Support/lsregister";
try {
  execFileSync(lsregister, ["-f", appDir], { stdio: "ignore" });
} catch {
  // Non-fatal: the app still works and ~/Applications gets indexed eventually.
}

console.log(`Installed ${APP_NAME} → ${appDir}`);
console.log(`Search "${APP_NAME}" in Spotlight or Raycast to launch it.`);
